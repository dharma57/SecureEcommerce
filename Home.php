         This became madonna, and its contraction mona. The title
         of the painting, though traditionally spelled "Mona" (as used by Vasari[5]), is also commonly spelled in modern Italian as Monna Lisa ("mona" being a vulgarity in some Italian dialects) but this is rare in English.[citation needed]

Vasari's account of the Mona Lisa comes from his biography of
Leonardo published in 1550, 31 years after the artist's death. 
It has long been the best-known source of information on the 
provenance of the work and identity of the sitter. Leonardo's 
assistant Salaì, at his death in 1525, owned a portrait which 
in his personal papers was named la Gioconda, a painting bequeathed
to him by Leonardo.That Leonardo painted such a work, and 
its date, were confirmed in 2005 when a scholar at Heidelberg
University discovered a marginal note in a 1477 printing of a 
volume written by the ancient Roman philosopher Cicero. Dated
October 1503, the note was written by Leonardo's contemporary
Agostino Vespucci. This note likens Leonardo to renowned Greek 
painter Apelles, who is mentioned in the text, and states that 
Leonardo was at that time working on a painting of Lisa del Giocondo.
